/**
  \fn          void USBH_VbusOnOff (bool vbus)
  \brief       Drive VBUS on/off.
  \param[in]   vbus
                - \b false VBUS off
                - \b true  VBUS on
  \return      none
*/
void USBH_VbusOnOff (bool vbus) {
  if (vbus) {                           // VBUS power on
    BSP_IO_WritePin(USBOTG_PPWR_GPIO_PIN, GPIO_PIN_RESET);
  } else {                              // VBUS power off
    BSP_IO_WritePin(USBOTG_PPWR_GPIO_PIN, GPIO_PIN_SET);
  }
}

/**
  \fn          bool USBH_GetOC (void)
  \brief       Get port over-current status.
  \return      port over-current status
                - \b false port is not in over-current status
                - \b true  port is in over-current status
*/
bool USBH_GetOC (void) {
  if ((BSP_IO_ReadPin(USBOTG_OC_GPIO_PIN) & USBOTG_OC_GPIO_PIN) == 0U) {
    return true;
  }
  return false;
}

